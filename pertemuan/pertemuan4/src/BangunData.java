class BangunDatar {
    // Luas persegi
    double hitungLuas(double sisi) {
        return sisi * sisi;
    }

    // Luas persegi panjang
    double hitungLuas(double panjang, double lebar) {
        return panjang * lebar;
    }

    // Luas lingkaran
    double hitungLuas(double jari, boolean lingkaran) {
        return 3.14 * jari * jari;
    }
}